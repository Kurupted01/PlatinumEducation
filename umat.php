    <p align="justify" class="style72"><strong>SECTION 1: Logical Reasoning and Problem Solving</strong><br>
      Questions in this section are based on a brief text or piece of information presented   graphically.&nbsp; Questions assess your ability to comprehend, draw logical   conclusions, reach solutions by identifying relevant facts, evaluate   information, pinpoint additional or missing information, and generate   and test plausible hypotheses.</p>
