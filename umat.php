      This section assesses the ability to understand and think about people.   Questions are based on a scenario, dialogue or other text representing   specific interpersonal situations. Most passages will have several   questions.The questions assess your ability to identify, understand,   and, where necessary, infer the thoughts, feelings, behaviour and/or   intentions of the people represented in the situations.</p>
